--------------------------------------------------------------------------------

    if ( this.bHandleInputs ( ) ) then
        object.sendEvent ( this.hSimpleCamera1 ( ),  "SimpleCameraAI", "onKeyboardKeyUp", kKeyCode )
    end
	
